#include "libft.h"

void			ft_putnbr(int n)
{
	char		c;
	long int	l;

	l = n;
	if (l < 0)
	{
		ft_putchar('-');
		l = -l;
	}
	if (l > 9)
		ft_putnbr(l / 10);
	c = l % 10 + '0';
	ft_putchar(c);
}
